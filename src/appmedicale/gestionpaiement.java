private void calculerStatistiques() {
    // Utiliser le modèle de jTable2
    DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
    
    int total = model.getRowCount();
    int payes = 0;
    int impayes = 0;
    double montantTotal = 0;
    
    for (int i = 0; i < total; i++) {
        String statut = (String) model.getValueAt(i, 6);  // Colonne 6 = Statut
        double prix = Double.parseDouble(((String) model.getValueAt(i, 5)).replace(" DH", ""));  // Colonne 5 = Prix
        
        if ("Payé".equals(statut)) {
            payes++;
        } else {
            impayes++;
        }
        montantTotal += prix;
    }
    
    System.out.println("Statistiques - Total: " + total + ", Payés: " + payes + 
                      ", Impayés: " + impayes + ", Montant: " + montantTotal + " DH");
}

