    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      try {
        // Récupérer tous les rendez-vous
        ConsultationDAO consultationDAO = new ConsultationDAO();
        java.util.List<Consultation> tousRendezVous = consultationDAO.getToutesConsultations();
        
        // Vider la table
        DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
        model.setRowCount(0);
        
        // Formateur de date
        java.text.SimpleDateFormat dateFormat = new java.text.SimpleDateFormat("dd/MM/yyyy HH:mm");
        
        // Ajouter chaque rendez-vous à la table
        for (Consultation rdv : tousRendezVous) {
            Object[] row = {
                rdv.getId(),
                dateFormat.format(rdv.getDateConsultation()),
                rdv.getPatient().getNom(),
                rdv.getPatient().getTelephone(),
                rdv.getCategorie().getDesignation(),
                rdv.getPrix() + " DH",
                rdv.getDescription(),
                rdv.getStatutPaiement() != null ? rdv.getStatutPaiement() : "Impayé"
            };
            model.addRow(row);
        }
        
        // Message de confirmation
        JOptionPane.showMessageDialog(this,
            tousRendezVous.size() + " rendez-vous chargés avec succès!",
            "Chargement réussi",
            JOptionPane.INFORMATION_MESSAGE);
            
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this,
            "Erreur lors du chargement: " + e.getMessage(),
            "Erreur",
            JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jButton1ActionPerformed

